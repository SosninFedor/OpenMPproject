#define _CRT_SECURE_NO_WARNINGS     // Отключаем предупреждения Microsoft о небезопасных функциях

#include <windows.h>                // Для SetConsoleOutputCP (кодировка русских букв)
#include <stdio.h>                  // Для printf (вывод на экран)
#include <stdlib.h>                 // Для malloc, free (выделение и освобождение памяти)
#include <omp.h>                     // Для OpenMP (параллельные директивы)
#include <time.h>                    // Для clock() (замер времени)

#define N 1000                       // Размер матрицы: 1000 строк и 1000 столбцов

// -----------------------------------------------------------------
// 1. ФУНКЦИИ ДЛЯ РАБОТЫ С МАТРИЦЕЙ И ВЕКТОРОМ
// -----------------------------------------------------------------

// Заполняет матрицу A и вектор b случайными числами от 0 до 9
void ProcessInit(double* A, double* b, int n) {     // A - матрица, b - вектор, n - размер
    for (int i = 0; i < n; i++) {                    // i - номер строки (от 0 до n-1)
        b[i] = rand() % 10;                           // rand() % 10 даёт число 0-9
        for (int j = 0; j < n; j++) {                 // j - номер столбца
            A[i * n + j] = rand() % 10;               // i*n+j переводит (i,j) в индекс одномерного массива
        }
    }
}

// Последовательное умножение матрицы A на вектор b, результат в c
void SerialProduct(double* A, double* b, double* c, int n) {  // A - матрица, b - вектор, c - результат
    for (int i = 0; i < n; i++) {                     // Для каждой строки i
        c[i] = 0.0;                                    // Обнуляем i-й элемент результата
        for (int j = 0; j < n; j++) {                  // Для каждого столбца j
            c[i] += A[i * n + j] * b[j];               // Умножаем и прибавляем: c[i] = сумма(A[i][j] * b[j])
        }
    }
}

// Параллельное умножение матрицы A на вектор b с помощью OpenMP
void ParallelProduct(double* A, double* b, double* c, int n) {
#pragma omp parallel for                            // ГЛАВНАЯ МАГИЯ: следующий цикл выполняют ВСЕ потоки одновременно
    for (int i = 0; i < n; i++) {                       // Каждый поток получает свои значения i
        c[i] = 0.0;
        for (int j = 0; j < n; j++) {
            c[i] += A[i * n + j] * b[j];
        }
    }
}

// -----------------------------------------------------------------
// 2. ФУНКЦИИ ДЛЯ РАБОТЫ С ДВУМЯ МАТРИЦАМИ
// -----------------------------------------------------------------

// Заполняет две матрицы A и B случайными числами
void InitMatrices(double* A, double* B, int n) {        // A, B - матрицы, n - размер
    for (int i = 0; i < n; i++) {                        // i - номер строки
        for (int j = 0; j < n; j++) {                    // j - номер столбца
            A[i * n + j] = rand() % 10;                  // Случайное число в A
            B[i * n + j] = rand() % 10;                  // Случайное число в B
        }
    }
}

// Последовательное умножение матрицы A на матрицу B, результат в C
void SerialMatrixMultiply(double* A, double* B, double* C, int n) {  // A,B - входные, C - результат
    for (int i = 0; i < n; i++) {                         // i - строка первой матрицы
        for (int j = 0; j < n; j++) {                     // j - столбец второй матрицы
            C[i * n + j] = 0.0;                            // Обнуляем элемент результата
            for (int k = 0; k < n; k++) {                  // k - внутренний индекс для умножения
                // Скалярное произведение i-й строки A и j-го столбца B
                C[i * n + j] += A[i * n + k] * B[k * n + j];
            }
        }
    }
}

// Параллельное умножение матрицы A на матрицу B
void ParallelMatrixMultiply(double* A, double* B, double* C, int n) {
#pragma omp parallel for collapse(2)                  // collapse(2) объединяет два цикла для лучшего распределения
    for (int i = 0; i < n; i++) {                         // Внешний цикл по строкам
        for (int j = 0; j < n; j++) {                     // Внутренний цикл по столбцам
            C[i * n + j] = 0.0;
            for (int k = 0; k < n; k++) {
                C[i * n + j] += A[i * n + k] * B[k * n + j];
            }
        }
    }
}

// -----------------------------------------------------------------
// 3. ФУНКЦИИ ЗАВЕРШЕНИЯ (ОСВОБОЖДЕНИЕ ПАМЯТИ)
// -----------------------------------------------------------------

// Освобождает память, выделенную под матрицу и векторы
void ProcessTerminate(double* A, double* b, double* c) {
    free(A);      // Освобождаем память матрицы
    free(b);      // Освобождаем память вектора b
    free(c);      // Освобождаем память вектора c
}

// Освобождает память, выделенную под две матрицы и результат
void TerminateMatrices(double* A, double* B, double* C) {
    free(A);      // Освобождаем память первой матрицы
    free(B);      // Освобождаем память второй матрицы
    free(C);      // Освобождаем память матрицы-результата
}

// -----------------------------------------------------------------
// 4. ГЛАВНАЯ ФУНКЦИЯ
// -----------------------------------------------------------------

int main() {
    // Устанавливаем кодировку Windows-1251 для правильного отображения русских букв
    SetConsoleOutputCP(1251);
    SetConsoleCP(1251);

    omp_set_num_threads(8);        // Говорим OpenMP использовать 8 потоков

    // Выводим шапку программы
    printf("========================================\n");
    printf("ЛАБОРАТОРНАЯ РАБОТА 2\n");
    printf("Размер матрицы: %d x %d\n", N, N);
    printf("Число потоков: %d\n", omp_get_max_threads());  // omp_get_max_threads() узнаёт текущее число потоков
    printf("========================================\n\n");

    clock_t start, end;             // Переменные для хранения меток времени
    double serial_time, parallel_time;  // Время выполнения в секундах

    // -----------------------------------------------------------------
    // ЭКСПЕРИМЕНТ 1: МАТРИЦА НА ВЕКТОР
    // -----------------------------------------------------------------

    printf("1. УМНОЖЕНИЕ МАТРИЦЫ НА ВЕКТОР\n");
    printf("----------------------------------------\n");

    // Выделяем память: N*N элементов для матрицы, N для векторов
    double* A = (double*)malloc(N * N * sizeof(double));   // sizeof(double) = 8 байт
    double* b = (double*)malloc(N * sizeof(double));
    double* c = (double*)malloc(N * sizeof(double));

    // Заполняем матрицу и вектор случайными числами
    ProcessInit(A, b, N);

    // Замер времени для последовательной версии
    start = clock();                    // Запоминаем время СЕЙЧАС
    SerialProduct(A, b, c, N);           // Выполняем умножение
    end = clock();                      // Запоминаем время ПОСЛЕ
    // Разницу (end-start) делим на CLOCKS_PER_SEC, чтобы получить секунды
    serial_time = (double)(end - start) / CLOCKS_PER_SEC;

    // Замер времени для параллельной версии
    start = clock();
    ParallelProduct(A, b, c, N);
    end = clock();
    parallel_time = (double)(end - start) / CLOCKS_PER_SEC;

    // Выводим результаты
    printf("  Последовательно: %.6f сек\n", serial_time);      // %.6f - 6 знаков после запятой
    printf("  Параллельно:     %.6f сек\n", parallel_time);
    printf("  Ускорение:       %.2f\n", serial_time / parallel_time);  // Во сколько раз быстрее

    // Проверяем первые 5 элементов, чтобы убедиться, что умножение сработало
    printf("  Первые 5 результатов: ");
    for (int i = 0; i < 5; i++) {
        printf("%.0f ", c[i]);           // %.0f - без знаков после запятой
    }
    printf("\n\n");

    // Освобождаем память после первого эксперимента
    ProcessTerminate(A, b, c);

    // -----------------------------------------------------------------
    // ЭКСПЕРИМЕНТ 2: МАТРИЦА НА МАТРИЦУ
    // -----------------------------------------------------------------

    printf("2. УМНОЖЕНИЕ МАТРИЦЫ НА МАТРИЦУ\n");
    printf("----------------------------------------\n");

    // Выделяем память для двух матриц и результата
    double* A2 = (double*)malloc(N * N * sizeof(double));
    double* B2 = (double*)malloc(N * N * sizeof(double));
    double* C2 = (double*)malloc(N * N * sizeof(double));

    // Заполняем матрицы случайными числами
    InitMatrices(A2, B2, N);

    // Замер времени для последовательного умножения матриц
    start = clock();
    SerialMatrixMultiply(A2, B2, C2, N);
    end = clock();
    serial_time = (double)(end - start) / CLOCKS_PER_SEC;

    // Замер времени для параллельного умножения матриц
    start = clock();
    ParallelMatrixMultiply(A2, B2, C2, N);
    end = clock();
    parallel_time = (double)(end - start) / CLOCKS_PER_SEC;

    // Выводим результаты
    printf("  Последовательно: %.6f сек\n", serial_time);
    printf("  Параллельно:     %.6f сек\n", parallel_time);
    printf("  Ускорение:       %.2f\n", serial_time / parallel_time);
    printf("  Первый элемент C[0][0] = %.0f\n", C2[0]);  // Проверяем один элемент

    // Освобождаем память после второго эксперимента
    TerminateMatrices(A2, B2, C2);


    printf("\n========================================\n");

    return 0;  // программа завершена 
}